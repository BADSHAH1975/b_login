class Product {
  final String id;
  final String name;
  final double price;
  final String description;
  final String type;
  final String imageUrl;
  final int likes;

  Product({
    required this.id,
    required this.name,
    required this.price,
    required this.description,
    required this.type,
    required this.imageUrl,
    required this.likes,
  });
}
